package com.laboratory.management.pojo;




public class Classroom {
	
	private String classroomId;
	
	private Integer studentNumber;
	
	private String classroomCampus;
	
	private String software;

	/**
	 * @return classroomId
	 */
	public String getClassroomId() {
		return classroomId;
	}

	/**
	 * @param classroomId 要设置的 classroomId
	 */
	public void setClassroomId(String classroomId) {
		this.classroomId = classroomId;
	}

	/**
	 * @return studentNumber
	 */
	public Integer getStudentNumber() {
		return studentNumber;
	}

	/**
	 * @param studentNumber 要设置的 studentNumber
	 */
	public void setStudentNumber(Integer studentNumber) {
		this.studentNumber = studentNumber;
	}

	/**
	 * @return classroomCampus
	 */
	public String getClassroomCampus() {
		return classroomCampus;
	}

	/**
	 * @param classroomCampus 要设置的 classroomCampus
	 */
	public void setClassroomCampus(String classroomCampus) {
		this.classroomCampus = classroomCampus;
	}

	/**
	 * @return software
	 */
	public String getSoftware() {
		return software;
	}

	/**
	 * @param software 要设置的 software
	 */
	public void setSoftware(String software) {
		this.software = software;
	}

	
}
